#include "libft.h"

void ft_bzero(void *s, size_t n)
{
    size_t i;
    unsigned char *byt;
    
    i = 0;
    byt = (unsigned char *)s;
    while (i < n)
    {
        byt[i] = 0;
        i++;
    }
    s = byt;
}
